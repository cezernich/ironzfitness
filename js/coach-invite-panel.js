// coach-invite-panel.js — Coach Portal "Invite" tab.
//
// Phase A: rendered the URL with copy / share. Phase C layers funnel
// counts (clicks → signups → accepted → active), an anonymized recent
// activity feed, a Rotate-code button, and a QR code for in-person
// sharing.
//
// Data sources:
//   • coach_invite_links — the coach's active link (one row).
//   • get_invite_funnel(link_id, days)        — Phase C RPC, 30d window.
//   • get_invite_recent_activity(link_id, n)  — Phase C RPC.
//   • rotate_invite_link()                    — Phase C RPC.
//
// QR code: rendered as an <img> from api.qrserver.com (free, no auth).
// External dependency, but the URL is still copyable if the service is
// down — coaches can read it aloud or paste it manually. Avoids
// bundling a QR library; we can vendor one in v2 if offline-first
// matters more than v1's launch ergonomics.

(function () {
  "use strict";

  // ironz.fit/c is the static landing page; ironz.app/c/<code> is the
  // future short URL once that domain points at the same redirect path.
  const SHARE_BASE = "https://ironz.fit/c";
  const QR_BASE = "https://api.qrserver.com/v1/create-qr-code/";
  const FUNNEL_DAYS = 30;
  const ACTIVITY_LIMIT = 8;

  // Module state — refreshed by load*. The funnel + activity blocks
  // render with whatever's cached, so flipping tabs doesn't strobe.
  let _link = null;            // { id, code, active, created_at }
  let _funnel = null;          // { clicks, signups, accepted, dismissed, active_clients }
  let _activity = null;        // [{ occurred_at, kind }, …]
  let _bio = "";               // profiles.coach_bio (≤500 chars)
  let _bioInitial = "";        // pristine value — used to detect dirty state
  let _bioSaving = false;
  let _bioMsg = "";            // transient "Saved" / error message
  let _loading = false;
  let _loadError = null;
  let _rotating = false;

  function _esc(s) {
    const div = document.createElement("div");
    div.textContent = s == null ? "" : String(s);
    return div.innerHTML;
  }

  // ── Public render entry ────────────────────────────────────────────────
  function renderCoachInvitePanel(state) {
    if (!_link && !_loading && !_loadError) {
      const coachUid = state?.coachUid || null;
      // Defer one tick so the loading skeleton actually paints.
      setTimeout(() => loadCoachInviteLink(coachUid), 0);
    }

    if (_loading) {
      return _wrapCard(`
        <div style="text-align:center;padding:32px;color:var(--color-text-muted);font-size:0.9rem">
          Loading your link…
        </div>`);
    }
    if (_loadError) {
      return _wrapCard(`
        <div style="padding:16px;color:var(--color-danger,#b91c1c);font-size:0.9rem">
          ${_esc(_loadError)}
        </div>`);
    }
    if (!_link) {
      return _wrapCard(`
        <div style="padding:16px;color:var(--color-text-muted);font-size:0.9rem">
          No active invite link found. If you were just promoted, refresh
          this page in a moment — it's auto-generated by the database.
        </div>`);
    }

    const url = _shareUrlFor(_link.code);
    return `
      <div class="card coach-section coach-invite-card">
        <div class="coach-section-title">Your invite link</div>
        <p class="hint" style="margin:0 0 12px">
          Share this link with prospective clients. They'll land on a
          personal page and can sign up or sign in to connect with you.
        </p>

        <div class="coach-invite-url-box">
          <div class="coach-invite-url" id="coach-invite-url">${_esc(url)}</div>
          <button class="btn-secondary btn-sm" id="coach-invite-copy-btn"
            onclick="copyCoachInviteLink()" aria-label="Copy invite link">
            Copy
          </button>
        </div>

        <div class="coach-invite-qr-row">
          <img class="coach-invite-qr"
               src="${_qrUrl(url)}"
               alt="QR code for ${_esc(url)}"
               width="160" height="160" loading="lazy" />
          <div class="coach-invite-qr-caption">
            <strong>Show this in person.</strong>
            Anyone scanning the code lands on your invite page.
          </div>
        </div>

        <div class="coach-invite-actions">
          <button class="btn-primary" onclick="shareCoachInviteLink()">Share link</button>
          <button class="btn-secondary" id="coach-invite-rotate-btn"
            onclick="rotateCoachInviteLink()"
            ${_rotating ? "disabled" : ""}>
            ${_rotating ? "Rotating…" : "Rotate code"}
          </button>
        </div>

        ${_renderBioEditor()}
        ${_renderFunnel()}
        ${_renderActivity()}
      </div>
    `;
  }

  // ── Bio editor ────────────────────────────────────────────────────────
  // Lives inside the Invite tab (not a separate Settings tab) because the
  // bio is exactly the text that shows up on the public landing page
  // visitors see — co-locating editor + preview-source removes a mental
  // hop. Photo upload deferred for v1 per spec Q3 (no Supabase Storage
  // wired yet); landing page falls back to initial-letter avatar.
  function _renderBioEditor() {
    const dirty = _bio !== _bioInitial;
    const remaining = 500 - (_bio || "").length;
    const overLimit = remaining < 0;
    const msg = _bioMsg
      ? `<span class="coach-invite-bio-msg${overLimit ? " coach-invite-bio-msg--err" : ""}">${_esc(_bioMsg)}</span>`
      : "";
    return `
      <div class="coach-invite-bio">
        <div class="coach-invite-funnel-title">Public bio</div>
        <p class="hint" style="margin:0 0 8px;font-size:0.82rem">
          Shows on your invite landing page. Visitors see this before deciding to sign up.
        </p>
        <textarea id="coach-invite-bio-input"
          class="coach-invite-bio-input"
          maxlength="500"
          rows="4"
          placeholder="e.g. I help endurance athletes hit their first 70.3 with structure and accountability."
          oninput="updateCoachInviteBioInput(this.value)"
        >${_esc(_bio)}</textarea>
        <div class="coach-invite-bio-row">
          <span class="coach-invite-bio-count${overLimit ? " coach-invite-bio-count--err" : ""}">
            ${remaining} char${remaining === 1 ? "" : "s"} left
          </span>
          ${msg}
          <button class="btn-primary btn-sm" id="coach-invite-bio-save"
            onclick="saveCoachInviteBio()"
            ${(!dirty || _bioSaving || overLimit) ? "disabled" : ""}>
            ${_bioSaving ? "Saving…" : "Save bio"}
          </button>
        </div>
      </div>
    `;
  }

  // Local-only state update on every keystroke. Re-renders so the
  // character counter + save button enable/disable in real time. We
  // don't auto-save — the explicit Save button matches how visitors
  // see the bio (publish-style, not draft-style).
  function updateCoachInviteBioInput(value) {
    _bio = String(value || "");
    _bioMsg = "";
    _refreshPanel();
    // Re-focus the textarea after the dashboard rerenders so typing
    // doesn't lose focus on every keystroke. The dashboard rebuild
    // replaces the DOM; we restore caret to the end of the input.
    setTimeout(() => {
      const ta = document.getElementById("coach-invite-bio-input");
      if (ta) {
        ta.focus();
        ta.setSelectionRange(ta.value.length, ta.value.length);
      }
    }, 0);
  }

  async function saveCoachInviteBio() {
    if (_bioSaving) return;
    if (_bio === _bioInitial) return;
    if (_bio.length > 500) return;

    const sb = window.supabaseClient;
    if (!sb) return;
    let userId;
    try {
      userId = (await sb.auth.getSession())?.data?.session?.user?.id;
    } catch {}
    if (!userId) return;

    _bioSaving = true;
    _bioMsg = "";
    _refreshPanel();

    try {
      const value = _bio.trim() ? _bio.trim() : null;
      const { error } = await sb
        .from("profiles")
        .update({ coach_bio: value })
        .eq("id", userId);
      if (error) {
        console.warn("[coach-invite-panel] bio save failed:", error);
        _bioMsg = "Couldn't save — try again.";
      } else {
        _bioInitial = _bio;
        _bioMsg = "Saved!";
        // Clear the "Saved!" toast after a moment so it doesn't sit
        // forever next to the (now-disabled) Save button.
        setTimeout(() => {
          if (_bioMsg === "Saved!") {
            _bioMsg = "";
            _refreshPanel();
          }
        }, 2000);
      }
    } catch (e) {
      console.warn("[coach-invite-panel] bio save threw:", e);
      _bioMsg = "Couldn't save — try again.";
    } finally {
      _bioSaving = false;
      _refreshPanel();
    }
  }

  function _wrapCard(inner) {
    return `
      <div class="card coach-section coach-invite-card">
        <div class="coach-section-title">Your invite link</div>
        ${inner}
      </div>`;
  }

  // ── Funnel block ──────────────────────────────────────────────────────
  // 5 bars (clicks / signups / accepted / dismissed / active). Bar widths
  // are normalized to clicks (the top of the funnel) so each step
  // visually shows attrition. "active" is a current snapshot, not 30-day
  // windowed — different mental model, kept in the same row but with a
  // muted tag so the comparison is honest.
  function _renderFunnel() {
    if (!_funnel) {
      return `
        <div class="coach-invite-funnel">
          <div class="coach-invite-funnel-title">Funnel · last ${FUNNEL_DAYS} days</div>
          <div class="coach-invite-funnel-loading">Loading stats…</div>
        </div>`;
    }
    const f = _funnel;
    const top = Math.max(1, f.clicks);

    const bar = (label, value, opts) => {
      const pct = Math.round((value / top) * 100);
      const tone = (opts && opts.tone) || "default";
      const sub = (opts && opts.sub) || "";
      return `
        <div class="coach-invite-funnel-row coach-invite-funnel-row--${tone}">
          <div class="coach-invite-funnel-label">${_esc(label)}${sub ? ` <span class="coach-invite-funnel-sub">${_esc(sub)}</span>` : ""}</div>
          <div class="coach-invite-funnel-bar">
            <div class="coach-invite-funnel-fill" style="width:${pct}%"></div>
          </div>
          <div class="coach-invite-funnel-value">${value}</div>
        </div>`;
    };

    const dismissedRow = f.dismissed > 0
      ? bar("Dismissed", f.dismissed, { tone: "muted", sub: "in 7-day cooldown" })
      : "";

    return `
      <div class="coach-invite-funnel">
        <div class="coach-invite-funnel-title">Funnel · last ${FUNNEL_DAYS} days</div>
        ${bar("Clicks", f.clicks)}
        ${bar("Signed up", f.signups)}
        ${bar("Accepted", f.accepted)}
        ${bar("Active clients", f.active_clients, { tone: "primary", sub: "right now" })}
        ${dismissedRow}
      </div>`;
  }

  // ── Activity feed ─────────────────────────────────────────────────────
  function _renderActivity() {
    if (!_activity) {
      return `
        <div class="coach-invite-activity">
          <div class="coach-invite-funnel-title">Recent activity</div>
          <div class="coach-invite-funnel-loading">Loading…</div>
        </div>`;
    }
    if (_activity.length === 0) {
      return `
        <div class="coach-invite-activity">
          <div class="coach-invite-funnel-title">Recent activity</div>
          <div class="coach-invite-funnel-loading">Nothing yet — share your link to get started.</div>
        </div>`;
    }

    const verbs = {
      clicked:   "Someone clicked",
      signed_up: "Someone signed up",
      accepted:  "Someone accepted",
      dismissed: "Someone dismissed",
      paired:    "Someone paired with you",
    };

    const rows = _activity.map((a) => {
      const verb = verbs[a.kind] || "Activity";
      const when = _relativeTime(a.occurred_at);
      return `<li><span class="coach-invite-activity-verb">${_esc(verb)}</span><span class="coach-invite-activity-when">${_esc(when)}</span></li>`;
    }).join("");

    return `
      <div class="coach-invite-activity">
        <div class="coach-invite-funnel-title">Recent activity</div>
        <ul class="coach-invite-activity-list">${rows}</ul>
        <p class="coach-invite-activity-note">Activity is anonymized for client privacy.</p>
      </div>`;
  }

  // ── Lazy load ──────────────────────────────────────────────────────────
  async function loadCoachInviteLink(coachUid) {
    const sb = window.supabaseClient;
    if (!sb) {
      _loadError = "Auth client unavailable.";
      _refreshPanel();
      return;
    }

    if (!coachUid) {
      try {
        const sess = (await sb.auth.getSession())?.data?.session;
        coachUid = sess?.user?.id || null;
      } catch {}
    }
    if (!coachUid) {
      _loadError = "Not signed in.";
      _refreshPanel();
      return;
    }

    _loading = true;
    _loadError = null;
    _refreshPanel();

    try {
      // Pull the link + the coach's bio in parallel so the panel renders
      // both blocks together. Keeping them as separate calls (not a
      // single join) keeps the schema honest — coach_invite_links has
      // no profile-shaped columns to denormalize.
      const [{ data: linkData, error: linkErr }, { data: profileData, error: profileErr }] = await Promise.all([
        sb.from("coach_invite_links")
          .select("id, code, active, created_at")
          .eq("coach_id", coachUid)
          .eq("active", true)
          .maybeSingle(),
        sb.from("profiles")
          .select("coach_bio")
          .eq("id", coachUid)
          .maybeSingle(),
      ]);

      if (linkErr) {
        console.warn("[coach-invite-panel] fetch failed:", linkErr);
        _loadError = "Couldn't load your invite link. Try refreshing.";
      } else if (!linkData) {
        _link = null;
      } else {
        _link = linkData;
        // Reset funnel/activity caches so we don't keep showing stats
        // tied to a previous (rotated) link.
        _funnel = null;
        _activity = null;
        _loadFunnel(_link.id);
        _loadActivity(_link.id);
      }

      if (profileErr) {
        console.warn("[coach-invite-panel] bio fetch failed:", profileErr);
      } else {
        _bio = (profileData && profileData.coach_bio) || "";
        _bioInitial = _bio;
      }
    } catch (e) {
      console.warn("[coach-invite-panel] fetch threw:", e);
      _loadError = "Couldn't load your invite link.";
    } finally {
      _loading = false;
      _refreshPanel();
    }
  }

  async function _loadFunnel(linkId) {
    const sb = window.supabaseClient;
    if (!sb || !linkId) return;
    try {
      const { data, error } = await sb.rpc("get_invite_funnel", {
        p_link_id: linkId,
        p_days: FUNNEL_DAYS,
      });
      if (error) {
        console.warn("[coach-invite-panel] funnel rpc error:", error);
        _funnel = { clicks: 0, signups: 0, accepted: 0, dismissed: 0, active_clients: 0 };
      } else {
        // RETURNS TABLE → array of one row.
        _funnel = Array.isArray(data) ? (data[0] || null) : data;
        if (!_funnel) {
          _funnel = { clicks: 0, signups: 0, accepted: 0, dismissed: 0, active_clients: 0 };
        }
      }
    } catch (e) {
      console.warn("[coach-invite-panel] funnel rpc threw:", e);
      _funnel = { clicks: 0, signups: 0, accepted: 0, dismissed: 0, active_clients: 0 };
    } finally {
      _refreshPanel();
    }
  }

  async function _loadActivity(linkId) {
    const sb = window.supabaseClient;
    if (!sb || !linkId) return;
    try {
      const { data, error } = await sb.rpc("get_invite_recent_activity", {
        p_link_id: linkId,
        p_limit: ACTIVITY_LIMIT,
      });
      if (error) {
        console.warn("[coach-invite-panel] activity rpc error:", error);
        _activity = [];
      } else {
        _activity = Array.isArray(data) ? data : [];
      }
    } catch (e) {
      console.warn("[coach-invite-panel] activity rpc threw:", e);
      _activity = [];
    } finally {
      _refreshPanel();
    }
  }

  function _refreshPanel() {
    if (typeof window.renderCoachDashboard === "function" && window._coachDashState) {
      window.renderCoachDashboard(window._coachDashState);
    }
  }

  function _shareUrlFor(code) {
    return `${SHARE_BASE}?code=${encodeURIComponent(code)}`;
  }
  function _qrUrl(targetUrl) {
    return `${QR_BASE}?size=200x200&data=${encodeURIComponent(targetUrl)}`;
  }

  // ── Copy + share ───────────────────────────────────────────────────────
  async function copyCoachInviteLink() {
    if (!_link) return;
    const url = _shareUrlFor(_link.code);
    try {
      await navigator.clipboard.writeText(url);
      _flashCopyButton("Copied!");
    } catch {
      const ta = document.createElement("textarea");
      ta.value = url;
      ta.style.cssText = "position:fixed;opacity:0";
      document.body.appendChild(ta);
      ta.select();
      try { document.execCommand("copy"); _flashCopyButton("Copied!"); } catch { _flashCopyButton("Copy failed"); }
      document.body.removeChild(ta);
    }
  }

  function _flashCopyButton(label) {
    const btn = document.getElementById("coach-invite-copy-btn");
    if (!btn) return;
    const original = btn.textContent;
    btn.textContent = label;
    btn.disabled = true;
    setTimeout(() => {
      btn.textContent = original;
      btn.disabled = false;
    }, 1500);
  }

  async function shareCoachInviteLink() {
    if (!_link) return;
    const url = _shareUrlFor(_link.code);
    const text = `Join my IronZ coaching: ${url}`;
    if (navigator.share) {
      try {
        await navigator.share({ title: "Join my IronZ coaching", text, url });
        return;
      } catch {
        // User cancelled OS sheet — fall through to clipboard.
      }
    }
    await copyCoachInviteLink();
  }

  // ── Rotate ────────────────────────────────────────────────────────────
  async function rotateCoachInviteLink() {
    if (!_link || _rotating) return;
    const sb = window.supabaseClient;
    if (!sb) return;

    const ok = (typeof confirm === "function")
      ? confirm("Rotate your invite code?\n\nYour current code stops working. Already-paired clients are unaffected. You'll need to re-share the new URL.")
      : true;
    if (!ok) return;

    _rotating = true;
    _refreshPanel();

    try {
      const { data, error } = await sb.rpc("rotate_invite_link");
      if (error) {
        console.warn("[coach-invite-panel] rotate rpc error:", error);
        if (typeof window._showShareToast === "function") {
          window._showShareToast("Couldn't rotate code — try again.");
        }
        return;
      }
      const row = Array.isArray(data) ? data[0] : data;
      if (row && row.new_link_id && row.new_code) {
        _link = { id: row.new_link_id, code: row.new_code, active: true, created_at: new Date().toISOString() };
        _funnel = null;
        _activity = null;
        _loadFunnel(_link.id);
        _loadActivity(_link.id);
        if (typeof window._showShareToast === "function") {
          window._showShareToast("New code generated!");
        }
      }
    } catch (e) {
      console.warn("[coach-invite-panel] rotate rpc threw:", e);
      if (typeof window._showShareToast === "function") {
        window._showShareToast("Couldn't rotate code — try again.");
      }
    } finally {
      _rotating = false;
      _refreshPanel();
    }
  }

  // ── Helpers ──────────────────────────────────────────────────────────
  function _relativeTime(iso) {
    if (!iso) return "";
    const then = new Date(iso).getTime();
    if (!Number.isFinite(then)) return "";
    const diff = Date.now() - then;
    const m = Math.round(diff / 60000);
    if (m < 1) return "just now";
    if (m < 60) return `${m} min ago`;
    const h = Math.round(m / 60);
    if (h < 24) return `${h} hour${h === 1 ? "" : "s"} ago`;
    const d = Math.round(h / 24);
    return `${d} day${d === 1 ? "" : "s"} ago`;
  }

  // ── Public surface ─────────────────────────────────────────────────────
  if (typeof window !== "undefined") {
    window.renderCoachInvitePanel    = renderCoachInvitePanel;
    window.loadCoachInviteLink       = loadCoachInviteLink;
    window.copyCoachInviteLink       = copyCoachInviteLink;
    window.shareCoachInviteLink      = shareCoachInviteLink;
    window.rotateCoachInviteLink     = rotateCoachInviteLink;
    window.updateCoachInviteBioInput = updateCoachInviteBioInput;
    window.saveCoachInviteBio        = saveCoachInviteBio;
  }
})();
